      SUBROUTINE FGetINTG(LINE, NDIGIT, IJ, KIJ, MTF)
      CHARACTER*80 LINE
      INTEGER NDIGIT
      INTEGER IJ(2)
      INTEGER KIJ(*)
      INTEGER MTF(4)
      IF(NDIGIT.EQ.2) THEN 
         READ(LINE,20) (IJ(J),J=1,2),(KIJ(K),K=1,18),(MTF(L),L=1,4)
      ELSE IF(NDIGIT.EQ.3) THEN
         READ(LINE,30) (IJ(J),J=1,2),(KIJ(K),K=1,13),(MTF(L),L=1,4)
      ELSE IF(NDIGIT.EQ.4) THEN
         READ(LINE,40) (IJ(J),J=1,2),(KIJ(K),K=1,11),(MTF(L),L=1,4)
      ELSE IF(NDIGIT.EQ.5) THEN
         READ(LINE,50) (IJ(J),J=1,2),(KIJ(K),K=1, 9),(MTF(L),L=1,4)
      ELSE IF(NDIGIT.EQ.6) THEN
         READ(LINE,60) (IJ(J),J=1,2),(KIJ(K),K=1, 8),(MTF(L),L=1,4)
      ELSE
         STOP 'ERROR - Invalid NDIGIT'
      END IF
 20   FORMAT (2I5,1X,18I3,1X,I4,I2,I3,I5)
 30   FORMAT (2I5,1X,13I4,3X,I4,I2,I3,I5)
 40   FORMAT (2I5,1X,11I5,   I4,I2,I3,I5)
 50   FORMAT (2I5,1X, 9I6,1X,I4,I2,I3,I5)
 60   FORMAT (2I5,    8I7,   I4,I2,I3,I5)
      END

      
